# 击破一名角色
# 可以触发对应属性的击破效果

@创建事件 造成击破 begin

    属性::标签.属性

    function 造成击破(事件::事件)
        new(事件.战场, 
        [事件.性质列表..., 击破伤害], 
        事件.来源, 事件.目标, 事件.来源.模板.属性)
    end

end

@触发事件 begin
    控制台换行("$事件")
    触发时机!(事件, 击破弱点时)
end

Base.show(io::IO, 事件::造成击破) = print(io, "【$(事件.来源)】将【$(事件.目标)】韧性击破！！")

function 造成击破!(事件::事件)
    触发(造成击破(事件))
end