# 消耗战技点的效果

@创建事件 消耗战技点 begin

    数值::Int

    function 消耗战技点(战场::战场; 目标::角色, 数值::Int = 1, 性质::Vector{标签.性质}=[])
        new(战场, 性质, 目标, 目标, 数值)
    end

end

function 持有战技点数量(事件::事件)::Int
    战技点(事件.战场, 事件.来源).当前
end

@触发事件 begin
    if 持有战技点数量(事件) < 事件.数值
        error("战技点不足")
    end
    战技点变化!(事件.战场, 角色 = 事件.目标, 数值 = - 事件.数值)
end

function 消耗战技点!(事件::事件; 目标 = 事件.目标, 数值::Int = 1)
    触发(消耗战技点(事件.战场, 目标 = 目标, 数值 = 数值, 性质 = 事件.性质列表))
end